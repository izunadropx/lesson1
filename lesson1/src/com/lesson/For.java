package com.lesson;

public class For {
    public static void main(String[] args) {

        for (int i = 0; i < 5; i++) { // loop counter (int i=0) ; condition ; last condition to be executed after the loop
            System.out.printf("The Value of i is: %d\n", i); //printf take 2 arguments (string ,(not+) value) %d will be replaced with value
        }

    }
}

